                    (Vergleich: Bitcoin - 21 Mio. Litecoin - 84 Mio. Monacoin - 152,2 Mio.) <br>
                    VIPS hat noch andere Vorteile:<br>
                    - Eines der Ziele ist es, Japans Nr. 1 auf dem Smart-Contract-Markt zu werden. Die hohe Anzahl sorgt für die Zweckmäßigkeit der Basis-Währung.<br>
                    - Coin-Burn gehört zu den Fundamentals. Stagniert der Preis, könnte das Coin-Burning den Wert steigern und die Währung liquide machen.<br>
                    Es ist geplant, dass die höchste Anzahl von 70 Mrd. im Jahr 2120 erreicht wird (53.611.200 Blöcke).<br>
                  </p>
                  <p class="modal-text">
                    2. Ausreichende Menge an VIPS werden für das Marketing und Entwicklung zurückgelegt. Somit haben wir ein sicheres Budget für Werbe- PR- Entwicklungstätigkeiten.
                  </p>
                  <p class="modal-text">
                    3. Ausreichende Menge an VIPS werden für "Nukumority"* zurückgelegt, somit sind wir in der Lage, konstant VIPS verteilen (vor allem an die VIPPER)**<br>
                    Dadurch sichern wir die Liquidität. (Übermäßige Verteilung wird vermieden, um den Kurssturz zu verhindern)
                  </p>
                  <p class="modal-text">
                    4. VIPS ist wie Monacoin eine auf die Community-basierende Währung. Durch eine starke Community wird ein großes Wachstum akkumuliert.
                  </p>
                  <p class="modal-text">
                    5. VIPS hat bereits feste Roadmap. Regelmäßig werden neue Ziele hinzugefügt, um die Währung weiterzuentwickeln. Dadurch bleibt die Roadmap stets aktuell und sie sorgt für graduelle Wertsteigerungen.
                  </p>
                  <p class="modal-text">
                    6. Als Smart-Contract-Währung ist VIPS in der Lage, weitere VIPS-Token zu generieren. Angenommen, jemand erstellt das VIPS-Token "Giko-Coin", müssten auch die Giko-Coin-Nutzer VIPS nutzen.
                  </p>
